#include <iostream>
#include <bits/stdc++.h> 
using namespace std;
int digit;
int xishu[105];
int sign[105];
int main()
{
	int start=1;
	cin>>digit;
	for(int i=digit;i>=0;i--)
	{
		cin>>xishu[i];
		if(xishu[i]>0&&xishu[i]!=1)
		{
			sign[i]=1;
		}//正数
		if(xishu[i]<0&&xishu[i]!=-1)
		{
			sign[i]=-1;
		}//负数
		if(xishu[i]==1)
		{
			sign[i]=2;
			//cout<<"true1";
		}//系数为1
		if(xishu[i]==-1)
		{
			sign[i]=-2;
			//cout<<"true2";
		}//系数为-1
	}
	for(int i=digit;i>=1;i--)
	{
		if(start!=1)
		{
			if(sign[i]==1)
			{
				cout<<"+"<<xishu[i]<<"x";
				if(i==1)
				{
					continue;
				}
				cout<<"^"<<i;
			}
			if(sign[i]==2)
			{
				cout<<"+x";
				if(i==1)
				{
					continue;
				}
				cout<<"^"<<i;
			}
			if(sign[i]==0)
			{
				continue;
			}
			if(sign[i]==-1)
			{
				cout<<xishu[i]<<"x";
				if(i==1)
				{
					continue;
				}
				cout<<"^"<<i;
			}
			if(sign[i]==-2)
			{
				cout<<"-x";
				if(i==1)
				{
					continue;
				}
				cout<<"^"<<i;
			}
		}
		if(start==1)
		{
			if(sign[i]==1)
			{
				cout<<xishu[i]<<"x";
				if(i==1)
				{
					continue;
				}
				cout<<"^"<<i;
			}
			if(sign[i]==2)
			{
				cout<<"x";
				if(i==1)
				{
					continue;
				}
				cout<<"^"<<i;
			}
			if(sign[i]==0)
			{
				continue;
			}
			if(sign[i]==-1)
			{
				cout<<xishu[i]<<"x";
				if(i==1)
				{
					continue;
				}
				cout<<"^"<<i;
			}
			if(sign[i]==-2)
			{
				cout<<"-x";
				if(i==1)
				{
					continue;
				}
				cout<<"^"<<i;
			}
			start=0;
		}
	}
	if(sign[0]!=0)
	{
		if(xishu
		[0]>0)
		cout<<"+";
		cout<<xishu[0];
	}
}
